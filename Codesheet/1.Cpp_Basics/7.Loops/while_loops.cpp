#include<bits/stdc++.h>
using namespace std;

int main() {
    // Now we will see while loop
    // while loop is used when we don't know the number of iterations
    // Here is how it is used
    int i = 1;
    while(i <= 10) {
        cout << i << endl;
        i++;
    }
    // This will print numbers from 1 to 10
    
    return 0;
}